// ---------------------------------------------------------------------------
// Challenge Template System
// ---------------------------------------------------------------------------
// Design principle — challenges vs. achievements:
//   Achievements = permanent "first-ever" or lifetime milestones
//                  ("your first C2 putt", "play 100 rounds lifetime", "first eagle")
//   Challenges   = repeatable weekly engagement tasks
//                  ("attempt 10 C2 putts in practice this week", "play 2 rounds at different courses")
//
// Every template here is intentionally framed as a THIS-WEEK task, not a milestone.
// This means someone who already unlocked the corresponding achievement will still
// find the challenge fresh and worthwhile.
//
// Pool size: 100 templates — 35 easy / 35 medium / 30 fun
// At 1 easy + 1 medium + 1 fun per week, with a 2-week recency buffer,
// this covers ~2+ years before any exact repeat is possible.
//
// Future expansion hooks (do not implement yet):
//   - daily challenges: add a `cadence: "daily" | "weekly"` field
//   - seasonal events: add a `season?: string` tag to scope templates
//   - sponsored / club challenges: add a `scope?: "global" | "club"` field
//   - difficulty tiers: add a `skillLevel?: "beginner" | "intermediate" | "advanced"` field
//   - XP multipliers: driven by external config, not hardcoded here
// ---------------------------------------------------------------------------

export type Difficulty = "easy" | "medium" | "fun";
export type ChallengeType = "boolean" | "count";
export type Category =
  | "putting"
  | "driving"
  | "rounds"
  | "accuracy"
